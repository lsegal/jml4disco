package org.jmlspecs.jml4.esc.distribution.configuration;

public class FrontControllerException extends Exception {

	public FrontControllerException() {
		super();
	}
	
	public FrontControllerException(String s) {
		super(s);
	}
	
	public FrontControllerException(Exception e) {
		super(e);
	}
	
	public FrontControllerException(String s, Exception e) {
		super(s, e);
	}
	
}
